package DurgaSoft.OperatorAssignment;

public class Assignment {
public static void main(String[] args) {
		
		// simple assignment
		int a =10, b = 20;
		System.out.println(a);
		
		// chain assignment
		int a1,a2,a3,a4;
		a1=a2=a3=a4=10;
		
		// Compound assignment
		int i = 10;
		i += 30;
		
}
}
